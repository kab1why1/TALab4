def main():
    # Створюємо порожній стек для результатів
    stack = []
    
    print("Введіть результати за останні 6 років (наприклад, середній бал за рік):")
    # Запис результатів у стек (порядок введення відповідає порядку перевірки)
    for i in range(6):
        try:
            score = float(input(f"Рік {i+1}: "))
        except ValueError:
            print("Будь ласка, введіть числове значення.")
            return
        stack.append(score)
    
    # Обробка робіт: імітуємо процес перевірки за допомогою операції pop
    # Поки стек не порожній, забираємо результати
    processed = []
    while stack:
        processed.append(stack.pop())
    
    # Оскільки елементи забиралися за принципом LIFO, 
    # повертаємо список, щоб отримати початковий порядок (від першого до шостого року)
    processed.reverse()
    
    print("\nАналіз трьох підряд років:")
    # Для кожної підпослідовності з 3 років проводимо аналіз
    # (при 6 роках таких підпослідовностей буде 4: роки 1-3, 2-4, 3-5, 4-6)
    for i in range(len(processed) - 2):
        three_years = processed[i:i+3]
        avg = sum(three_years) / 3
        
        # Аналіз динаміки: якщо результати строго зростають - покращення,
        # якщо строго спадають - погіршення, інакше - нестабільна ситуація.
        if three_years[0] < three_years[1] < three_years[2]:
            trend = "Покращення"
        elif three_years[0] > three_years[1] > three_years[2]:
            trend = "Погіршення"
        else:
            trend = "Нестабільна"
        
        print(f"Роки {i+1}-{i+3}: {three_years} | Середній бал: {avg:.2f} | Тенденція: {trend}")

if __name__ == "__main__":
    main()
